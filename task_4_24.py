'''В фермерском хозяйстве в Карелии выращивают чернику. Она растет на круглой грядке, причем кусты высажены только по окружности. 
Таким образом, у каждого куста есть ровно два соседних. Всего на грядке растет N кустов. Эти кусты обладают разной 
урожайностью, поэтому ко времени сбора на них выросло различное число ягод – на i-ом кусте выросло ai ягод 
В этом фермерском хозяйстве внедрена система автоматического сбора черники. Эта система состоит из управляющего модуля и нескольких собирающих модулей. Собирающий модуль за один заход, находясь непосредственно перед некоторым кустом, собирает ягоды с этого куста и с двух соседних с ним. Напишите программу для нахождения максимального числа ягод, которое может собрать за один заход собирающий модуль, 
находясь перед некоторым кустом заданной во входном файле грядки.'''

# чтение данных из файла
with open('input.txt', 'r') as f:
    n = int(f.readline().strip())
    a = list(map(int, f.readline().strip().split()))

# вычисление префиксных сумм
s = [0] * (n + 1)
for i in range(1, n + 1):
    s[i] = s[i - 1] + a[i - 1]

# поиск максимальной суммы ягод
max_sum = 0
for i in range(n - 2):
    curr_sum = s[i + 3] - s[i]
    if curr_sum > max_sum:
        max_sum = curr_sum

# вывод результата
print(max_sum)





